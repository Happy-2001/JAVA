package model;

/**
 *
 * @author Happy-2001
 */
public class Login {
    public String name;
    public String pass;
    public Login(){
        
    }

    public Login(String name, String pass) {
        this.name = name;
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    @Override
    public String toString() {
        return "Login{" + "name=" + name + ", pass=" + pass + '}';
    }
    
}
